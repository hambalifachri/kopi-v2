const fs = require("fs");
const fsp = require("fs/promises");
const http = require("http");
const path = require("path");
const { exec } = require("child_process");

const root = path.resolve(__dirname, "..");
const uploadDir = path.join(root, "assets", "testimoni");
const preferredPort = Number(process.argv[2] || process.env.PORT || 4173);
const host = "127.0.0.1";
let activePort = preferredPort;
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp"]);
const mimeExtensions = {
  "image/jpeg": ".jpg",
  "image/png": ".png",
  "image/webp": ".webp",
};
const contentTypes = {
  ".html": "text/html; charset=utf-8",
  ".css": "text/css; charset=utf-8",
  ".js": "text/javascript; charset=utf-8",
  ".json": "application/json; charset=utf-8",
  ".jpg": "image/jpeg",
  ".jpeg": "image/jpeg",
  ".png": "image/png",
  ".webp": "image/webp",
};

function sendJson(res, status, payload) {
  res.writeHead(status, { "Content-Type": "application/json; charset=utf-8" });
  res.end(JSON.stringify(payload));
}

function safeFileName(name, index) {
  const extFromName = path.extname(name).toLowerCase();
  const ext = imageExtensions.has(extFromName) ? extFromName : ".jpg";
  const base = path.basename(name, extFromName)
    .normalize("NFKD")
    .replace(/[^\w.-]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "")
    .slice(0, 60) || "testimoni";
  return `${Date.now()}-${index + 1}-${base}${ext}`;
}

function parseMultipart(buffer, boundary) {
  const boundaryText = `--${boundary}`;
  const parts = buffer.toString("binary").split(boundaryText).slice(1, -1);

  return parts.map((part) => {
    const trimmed = part.replace(/^\r\n/, "").replace(/\r\n$/, "");
    const separatorIndex = trimmed.indexOf("\r\n\r\n");
    if (separatorIndex === -1) return null;

    const rawHeaders = trimmed.slice(0, separatorIndex);
    const rawBody = trimmed.slice(separatorIndex + 4);
    const disposition = rawHeaders.match(/content-disposition:\s*form-data;([^\r\n]+)/i)?.[1] || "";
    const type = rawHeaders.match(/content-type:\s*([^\r\n]+)/i)?.[1]?.trim() || "";
    const name = disposition.match(/name="([^"]+)"/)?.[1] || "";
    const filename = disposition.match(/filename="([^"]*)"/)?.[1] || "";

    return {
      name,
      filename,
      type,
      body: Buffer.from(rawBody, "binary"),
    };
  }).filter(Boolean);
}

async function readBody(req) {
  const chunks = [];
  for await (const chunk of req) chunks.push(chunk);
  return Buffer.concat(chunks);
}

function isTestimonialImage(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  const baseName = path.basename(filePath).toLowerCase();
  if (!imageExtensions.has(ext)) return false;
  if (baseName.includes("qris")) return false;
  if (baseName.startsWith("icon-")) return false;
  return true;
}

function toBrowserPath(filePath) {
  return path.relative(root, filePath).split(path.sep).map(encodeURIComponent).join("/");
}

async function listImagesIn(folder) {
  try {
    const entries = await fsp.readdir(folder, { withFileTypes: true });
    return entries
      .filter((entry) => entry.isFile())
      .map((entry) => path.join(folder, entry.name))
      .filter(isTestimonialImage);
  } catch {
    return [];
  }
}

async function updateGalleryManifest() {
  const folders = [path.join(root, "assets"), uploadDir];
  const imagePaths = (await Promise.all(folders.map(listImagesIn))).flat();
  imagePaths.sort((a, b) => a.localeCompare(b, "id", { numeric: true, sensitivity: "base" }));

  const browserPaths = [...new Set(imagePaths.map(toBrowserPath))];
  const output = [
    "// Auto-generated by tools/testimonial-admin-server.js",
    "// Use admin-testimoni.html to add more screenshot files.",
    `window.TESTIMONIAL_IMAGES = ${JSON.stringify(browserPaths, null, 2)};`,
    "",
  ].join("\n");
  await fsp.writeFile(path.join(root, "gallery-images.js"), output);
  return browserPaths;
}

async function handleUpload(req, res) {
  const contentType = req.headers["content-type"] || "";
  const boundary = contentType.match(/boundary=(.+)$/)?.[1];
  if (!boundary) {
    sendJson(res, 400, { error: "Upload tidak valid." });
    return;
  }

  await fsp.mkdir(uploadDir, { recursive: true });
  const body = await readBody(req);
  const parts = parseMultipart(body, boundary)
    .filter((part) => part.name === "screenshots" && part.filename && part.body.length > 0)
    .filter((part) => part.type.startsWith("image/") || imageExtensions.has(path.extname(part.filename).toLowerCase()));

  if (!parts.length) {
    sendJson(res, 400, { error: "Pilih minimal 1 gambar." });
    return;
  }

  let uploaded = 0;
  for (const [index, part] of parts.entries()) {
    const fallbackExt = mimeExtensions[part.type] || ".jpg";
    const fileName = safeFileName(part.filename || `testimoni${fallbackExt}`, index);
    await fsp.writeFile(path.join(uploadDir, fileName), part.body);
    uploaded += 1;
  }

  const images = await updateGalleryManifest();
  sendJson(res, 200, { uploaded, total: images.length });
}

async function serveStatic(req, res) {
  const url = new URL(req.url || "/", `http://${host}:${activePort}`);
  const requestPath = decodeURIComponent(url.pathname === "/" ? "/admin-testimoni.html" : url.pathname);
  const filePath = path.resolve(root, requestPath.slice(1));
  const rootWithSep = root.endsWith(path.sep) ? root : `${root}${path.sep}`;

  if (filePath !== root && !filePath.startsWith(rootWithSep)) {
    res.writeHead(403);
    res.end("Forbidden");
    return;
  }

  try {
    const data = await fsp.readFile(filePath);
    const type = contentTypes[path.extname(filePath).toLowerCase()] || "application/octet-stream";
    res.writeHead(200, { "Content-Type": type });
    res.end(data);
  } catch {
    res.writeHead(404);
    res.end("Not found");
  }
}

const server = http.createServer(async (req, res) => {
  try {
    if (req.method === "POST" && req.url === "/api/testimonials") {
      await handleUpload(req, res);
      return;
    }

    await serveStatic(req, res);
  } catch (error) {
    sendJson(res, 500, { error: error.message || "Server error." });
  }
});

async function onReady(port) {
  activePort = port;
  await fsp.mkdir(uploadDir, { recursive: true });
  await updateGalleryManifest();
  const url = `http://localhost:${port}/admin-testimoni.html`;
  console.log(`Admin testimoni siap: ${url}`);
  if (process.env.NO_OPEN !== "1") {
    exec(`start "" "${url}"`, { shell: "cmd.exe" });
  }
}

function listen(port) {
  server.once("error", (error) => {
    if (error.code === "EADDRINUSE" && port < preferredPort + 10) {
      listen(port + 1);
      return;
    }
    console.error(error.message);
    process.exit(1);
  });

  server.listen(port, host, () => onReady(port));
}

listen(preferredPort);
