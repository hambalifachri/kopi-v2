const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp"]);
const folders = [
  path.join(root, "assets"),
  path.join(root, "assets", "New folder"),
  path.join(root, "assets", "testimoni"),
];

function isTestimonialImage(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  const baseName = path.basename(filePath).toLowerCase();
  if (!imageExtensions.has(ext)) return false;
  if (baseName.includes("qris")) return false;
  return true;
}

function toBrowserPath(filePath) {
  const relativePath = path.relative(root, filePath).split(path.sep);
  return relativePath.map(encodeURIComponent).join("/");
}

const imagePaths = folders.flatMap((folder) => {
  if (!fs.existsSync(folder)) return [];
  return fs.readdirSync(folder, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => path.join(folder, entry.name))
    .filter(isTestimonialImage);
});

imagePaths.sort((a, b) => a.localeCompare(b, "id", { numeric: true, sensitivity: "base" }));

const browserPaths = [...new Set(imagePaths.map(toBrowserPath))];
const output = [
  "// Auto-generated by tools/update-testimonials.js",
  "// Put testimonial screenshots in assets/New folder or assets/testimoni, then run: node tools/update-testimonials.js",
  `window.TESTIMONIAL_IMAGES = ${JSON.stringify(browserPaths, null, 2)};`,
  "",
].join("\n");

fs.writeFileSync(path.join(root, "gallery-images.js"), output);
console.log(`Updated gallery-images.js with ${browserPaths.length} image(s).`);
